public class Operators {
    public static void main(String[] args){
        int number1 = 12;
        int number2 = 6;

        double number3 = 12;
        double number4 = 5;

//        addition
        System.out.println(number1 + number2);

//        subtraction
        System.out.println(number1 - number2);

        //        multiplication
        System.out.println(number1 * number2);

        //        division
        System.out.println(number1 / number2);
        System.out.println(number3 / number4);


//        modulus
        System.out.println(number1 % number3);
    }
}
